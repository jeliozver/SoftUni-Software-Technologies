package IntersectionOfCircles;

public class Circle {
    private double Radius;
    private Point Center;

    public double getRadius() {
        return Radius;
    }

    public void setRadius(double radius) {
        Radius = radius;
    }

    public Point getCenter() {
        return Center;
    }

    public void setCenter(Point center) {
        Center = center;
    }
}